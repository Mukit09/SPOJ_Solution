int tree[3*si],u,v;

void build(int node,int lft,int rht)
{
    if(lft==rht)
    {
        tree[node]=a[lft];
        return ;
    }
    int lc,rc,m;
    m=(lft+rht)>>1;
    lc=node<<1;
    rc=lc+1;
    build(lc,lft,m);
    build(rc,m+1,rht);
    tree[node]=tree[lc]<tree[rc]?tree[lc]:tree[rc];
}

int query(int node,int lft,int rht)
{
    if(lft>v||rht<u)
    return inf;
    if(lft>=u&&rht<=v)
    return tree[node];
    int lc,rc,m,p1,p2;
    m=(lft+rht)>>1;
    lc=node<<1;
    rc=lc+1;
    p1=query(lc,lft,m);
    p2=query(rc,m+1,rht);
    return p1<p2?p1:p2;
}

